    public static function getNavigationBadge(): ?string
    {
        $unreviewed = static::getModel()::unreviewed()->count();

        return $unreviewed > 0 ? (string) $unreviewed : null;
    }

    public static function getNavigationBadgeColor(): string|array|null
    {
        $unreviewed = static::getModel()::unreviewed()->count();

        if ($unreviewed > 10) {
            return 'danger';
        }
        if ($unreviewed > 5) {
            return 'warning';
        }
        if ($unreviewed > 0) {
            return 'info';
        }

        return null;
    }
